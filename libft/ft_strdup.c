#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	len;
	char	*ptr;

	len = ft_strlen(s1) + 1;
	ptr = malloc(len);
	if (ptr)
		ft_memcpy(ptr, s1, len);
	return (ptr);
}
